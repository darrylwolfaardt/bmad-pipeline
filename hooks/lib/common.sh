# ---------------------------------------------------------------------------
# BMAD command/template lookup
# ---------------------------------------------------------------------------
find_bmad_file() {
  local name="$1"
  for dir in "${BMAD_COMMAND_DIRS[@]}"; do
    for ext in ".md" ".txt" ""; do
      [[ -f "${dir}/${name}${ext}" ]] && { echo "${dir}/${name}${ext}"; return 0; }
    done
  done
  return 1
}
